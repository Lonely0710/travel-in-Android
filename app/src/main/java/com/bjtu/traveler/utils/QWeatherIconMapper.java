    /**
     * Gets the Unicode character for a given QWeather icon code.
     *
     * @param iconCode The icon code from QWeather API.
     * @return The corresponding Unicode character string, or "?" if not found.
     */
    public static String getIconCharacter(String iconCode) {
        // Remove potential day/night suffix if present (e.g., '100' instead of '100d'/'100n')
        String baseIconCode = iconCode;
        if (baseIconCode != null && (baseIconCode.endsWith("d") || baseIconCode.endsWith("n"))) {
             baseIconCode = baseIconCode.substring(0, baseIconCode.length() - 1);
        }
        // Look up the character in the map, default to "?" if not found
        return iconMap.getOrDefault(baseIconCode, "?");
    }
